            I'm Darlene M. Curry, M.Ed., M.A., L.P.C.  I've had the opportunity to live in the panhandle of Texas for twenty plus years.  In that time I have taught high school English, served as District Counselor, served as an Elementary Principal, and my greatest joy--served as a licensed counselor.  My education includes a Bachelor of Science degree from Wayland Baptist University, a Master's In Education from Northwestern Oklahoma State University, and a Master's in Counseling from Wayland Baptist University.  I am licensed with the State Board of Professional Counselors and I have enjoyed serving the panhandle for the last ten years.  Helping people find their way is PURE JOY for me!  I love working with people of all ages.​
